void draw_line(t_settings *data, double angle, double line_lenght, long color)
{
	int x1;
	int y1;

	for(int i = 0; i < line_lenght; i += 1)
    {
		x1 = i * sin(angle) + data->posX;
		y1 = i * cos(angle) + data->posY;
		if (x1 < screenWidth && x1 > 0  && y1 < screenHeight && y1 > 0)
			mlx_put_pixel(data->img, x1, y1, color);
	}
}

void draw_player_point(t_settings *data)
{
	int x1;
	int y1;

	for(int i = 0; i < 360; i += 1)
    {
		for (int r = 0; r < 6; r++)
		{
            x1 = r * cos(i * PI / 180);
            y1 = r * sin(i * PI / 180);
            mlx_put_pixel(data->img, data->posX + x1, data->posY + y1, 0x00ffff00);
      	}
	}
	draw_line(data, data->angle, 50, 0x00ffff00);
}

